    private void pwdJtxt1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pwdJtxt1KeyReleased
        // TODO add your handling code here:
        char[] pwdList=pwdJtxt1.getPassword();
        String pwd= String.valueOf(pwdList);
        String pwdStrength = Validate.passwordStrengthCheck(usernameJtxt.getText(), pwd);
        int pwdStrengthInt = Integer.valueOf(pwdStrength);
        if (pwdStrengthInt < 15) {
            pwdProgressBar.setValue(Integer.valueOf(pwdStrength));
            progressbar(Color.RED);
            pwdStrengthText.setText("VERY WEAK");
            pwdStrengthText.setForeground(Color.red);
        } else if (pwdStrengthInt < 50) {
            pwdProgressBar.setValue(Integer.valueOf(pwdStrength));
            progressbar(Color.ORANGE);
            pwdStrengthText.setText("WEAK");
            pwdStrengthText.setForeground(Color.ORANGE);
        } else if (pwdStrengthInt < 85) {
            pwdProgressBar.setValue(Integer.valueOf(pwdStrength));
            progressbar(Color.green);
            pwdStrengthText.setText("MEDIUM");
            pwdStrengthText.setForeground(Color.green);
        } else if (pwdStrengthInt <= 100) {
            pwdProgressBar.setValue(Integer.valueOf(pwdStrength));
            progressbar(new Color(0, 127, 82));
            pwdStrengthText.setText("STRONG");
            pwdStrengthText.setForeground(new Color(0, 127, 82));
        }
    }//GEN-LAST:event_pwdJtxt1KeyReleased

