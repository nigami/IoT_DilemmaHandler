    public void populateNetwork() {
        nwCombo.removeAllItems();
        for (Network nw : ecosystem.getNetworkList()) {
            nwCombo.addItem(nw);
        }
    }

    public void populateEnterprise(Network nw) {
        enterpriseCombo.removeAllItems();
        for (Enterprise entr : nw.getEnterpriseDirectory().getEnterpriseList()) {
            if (entr instanceof CommunityEnterprise) {
                enterpriseCombo.addItem(entr);
            }
        }
    }

