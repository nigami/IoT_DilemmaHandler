    public void CheckAddFinanciallyStruckPeople(UserAccount acc) {
        for (Network nw : ecosystem.getNetworkList()) {
            if (nw.equals((Network) nwCombo.getSelectedItem())) {
                for (Enterprise ent : nw.getEnterpriseDirectory().getEnterpriseList()) {
                    if (ent instanceof FinancialEnterprise) {
                        float minMonWage = ((FinancialEnterprise) ent).getMinimumWage();
                        if ((!familyIncomeMonJTxt.getText().isEmpty()) && minMonWage > 0) {
                            if (minMonWage > Float.valueOf(familyIncomeMonJTxt.getText())) {
                                ((FinancialEnterprise) ent).getFinancialChallengedDirectory().addUserAccount(acc);
                            }
                        }
                    }
                }
            }
        }
    }
