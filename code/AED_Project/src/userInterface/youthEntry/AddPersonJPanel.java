    public String ValidateProfile() {

        String errMsg = new String();
        if ((nwCombo.getSelectedItem() == null || enterpriseCombo.getSelectedItem() == null) && usernameJtxt.getText().isEmpty()) {
            errMsg = errMsg + "No Country and community is associated with current personnel\n";
            return (errMsg).trim();
        }

        if (personNameJTxt.getText().isEmpty()) {
            errMsg = errMsg + "Name is manadatory\n";
        } else {
            if (!personNameJTxt.getText().matches("[a-zA-Z ]*")) {
                errMsg = errMsg + "Name is not a valid input\n";
            }
        }

        if (dobJTxt.getDate() == null) {
            errMsg = errMsg + "Date of Birth is either empty or invalid\n";
        } else {
            if (dobJTxt.getDate().after(new Date())) {
                errMsg = errMsg + "Date of Birth cannot be future date\n";
            } else if (AgeCalculator.calculateAge(dobJTxt.getDate()) <= 2) {
                errMsg = errMsg + "Date of Birth suggests you are younger than 2years, we dont support age<3years \n";
            }
        }

        if (!Validate.ValidateFloat(familyExpMonJTxt.getText(), null)) {
            errMsg = errMsg + "Family Monthly Expenses should be non-negative numberic\n";
        }

        if (!Validate.ValidateFloat(familyIncomeMonJTxt.getText(), null)) {
            errMsg = errMsg + "Family Monthly Income should be non-negative numberic\n";
        }

        if (!Validate.ValidateEmpty(gpatxt.getText(), null)) {
            errMsg = errMsg + "GPA is manadatory\n";
        } else if (!Validate.ValidateFloat(gpatxt.getText(), null)) {
            errMsg = errMsg + "GPA should be Numberic\n";
        }else if ((Float.valueOf(gpatxt.getText())>4) || (Float.valueOf(gpatxt.getText())<0)){
            errMsg = errMsg + "GPA should not be more than 4\n";
        }

        if (stAddTextArea.getText().isEmpty()) {
            errMsg = errMsg + "Street Address is manadatory\n";
        }
        if (townJText.getText().isEmpty()) {
            errMsg = errMsg + "Town is manadatory\n";
        } else {
            if (!townJText.getText().matches("[a-zA-Z_0-9]*")) {
                errMsg = errMsg + "Town is not a valid input\n";
            }
        }
        if (zipJText.getText().isEmpty()) {
            errMsg = errMsg + "Zipcode is manadatory\n";
        } else {
            if (!zipJText.getText().matches("\\d{5}(-\\\\d{4})?")) {
                errMsg = errMsg + "Zipcode can valid 5 digit US Zipcode\n";
            }
            if (!(zipJText.getText().length() == 5)) {
                errMsg = errMsg + "Zip Code should be 5 digits\n";
            }
        }
        if (emailJText1.getText().isEmpty()) {
            errMsg = errMsg + "Email is manadatory\n";
        } else {
            if (!Validate.ValidateEmail(emailJText1.getText(), null)) {
                errMsg = errMsg + "Email is not valid\n";
            }
        }

        if (occuJText.getText().isEmpty()) {
            errMsg = errMsg + "Occupation is manadatory\n";
        }

        if (areaCdeJText.getText().isEmpty()) {
            errMsg = errMsg + "Area code is manadatory\n";
        } else {
            if (!areaCdeJText.getText().matches("[0-9]*")) {
                errMsg = errMsg + "Area code is integer only\n";
            }
            if (!(areaCdeJText.getText().length() == 3)) {
                errMsg = errMsg + "Area code should be 3 digits\n";
            }
        }
        if (phNumJText.getText().isEmpty()) {
            errMsg = errMsg + "Phone Number is manadatory";
        } else {
            if (!phNumJText.getText().matches("[0-9]*")) {
                errMsg = errMsg + "Phone number is integer only\n";
            }
            if (!(phNumJText.getText().length() == 10)) {
                errMsg = errMsg + "Phone number should be 10 digits\n";
            }else if(Long.valueOf(phNumJText.getText())<1000000000){
                 errMsg = errMsg + "Phone number is not valid\n";
            }
        }

        return errMsg;
    }
