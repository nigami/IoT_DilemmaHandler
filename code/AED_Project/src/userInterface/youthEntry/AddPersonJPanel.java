    private void createUAJBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUAJBtnActionPerformed
        // TODO add your handling code here:
        char[] passwordCharArray = pwdJtxt1.getPassword();
        String password = String.valueOf(passwordCharArray);
        if (Validate.ValidateEmpty(password, "Password is manadatory") && Validate.ValidateEmpty(usernameJtxt.getText(), "Username is manadatory")) {
            String pwdStrength = Validate.passwordStrengthCheck(usernameJtxt.getText(), password);

            if (Integer.valueOf(pwdStrength) < 15) {
                pwdProgressBar.setValue(Integer.valueOf(pwdStrength));
                progressbar(Color.RED);
                JOptionPane.showMessageDialog(null, "Password is very weak!", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                Enterprise ent = (Enterprise) enterpriseCombo.getSelectedItem();
                for (Organization org : ent.getOrganizationDirectory().getOrganizationList()) {
                    if (org instanceof StudentOrganization) {
                        if (org.getUserAccountDirectory().checkIfUsernameIsUnique(usernameJtxt.getText())) {
                            // System.err.println("useracc person before......."+this.personProfile.getName());
                            UserAccount acc = org.getUserAccountDirectory().createUserAccount(usernameJtxt.getText(), password, this.personProfile, (StudentRole) org.getSupportedRole().get(0));
                            //System.err.println("useracc person......."+acc.getPerson().getName());
                            CheckAddFinanciallyStruckPeople(acc);

                            JOptionPane.showMessageDialog(null, "Welcome aboard " + usernameJtxt.getText() + " !", "Congratulations!!", JOptionPane.INFORMATION_MESSAGE);
                            disableUserAccount();
                            this.useracc = acc;
                            continueJBtn.setVisible(true);

                            manageLoginMenu();
                        } else {
                            JOptionPane.showMessageDialog(null, "Username already exsist, Please try something else!", "Abort!!", JOptionPane.ERROR_MESSAGE);
                        }
                    }
                }
            }
        }

    }//GEN-LAST:event_createUAJBtnActionPerformed
