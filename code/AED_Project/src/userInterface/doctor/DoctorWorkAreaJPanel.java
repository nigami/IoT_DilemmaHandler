    private void viewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBtnActionPerformed
        // TODO add your handling code here:'
        DefaultTableModel dtm = (DefaultTableModel) workRequestJTable.getModel();
        if (workRequestJTable.getSelectedRow() > -1) {
            WorkRequest workRequest = (WorkRequest) dtm.getValueAt(workRequestJTable.getSelectedRow(), 0);
            String donorName = ((PersonalHealthIssueWorkRequest) workRequest).getSender().getUsername().toString();
            qrCodelabel.setIcon(new ImageIcon(donorName + ".jpg"));
            donorNameJtxt.setText(((PersonalHealthIssueWorkRequest) workRequest).getSender().getPerson().getName().toString());
            resultJtxt.setText(((PersonalHealthIssueWorkRequest) workRequest).getTestResult());
            dateReqJTxt.setText(String.valueOf(workRequest.getRequestDate().toString()));
            reqMessage.setText(workRequest.getMessage());
            statusJTxt.setText(workRequest.getStatus());
            dateresolvedJTxt.setText(workRequest.getResolveDate().toString());
        }
    }//GEN-LAST:event_viewBtnActionPerformed

