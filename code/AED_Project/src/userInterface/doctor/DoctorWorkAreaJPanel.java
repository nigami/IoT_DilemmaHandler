    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();
        model.setRowCount(0);

        for (WorkRequest request : org.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[8];
            row[0] = request;
            row[1] = request.getSender().getPerson().getName();

            row[2] = request.getRequestDate();
            row[3] = request.getMessage();
            row[4] = (((PersonalHealthIssueWorkRequest) request).getUploadList().size() > 0) ? "Yes" : "No";

            row[5] = request.getStatus();
            row[6] = request.getReceiver() == null ? null : request.getReceiver().getPerson().getName();
            row[7] = request.getResolveDate();
            model.addRow(row);
        }
    }

