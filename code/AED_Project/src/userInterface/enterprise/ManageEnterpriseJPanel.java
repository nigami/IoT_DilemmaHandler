    public void populateEnterprise() {
        DefaultTableModel dtm = (DefaultTableModel) enterpriseJTable.getModel();
        dtm.setRowCount(0);

        Object row[] = new Object[3];
        for (Network nw : system.getNetworkList()) {
            if (nw != null) {
                for (Enterprise ent : nw.getEnterpriseDirectory().getEnterpriseList()) {
                    row[1] = nw.getName();
                    row[0] = ent.getName();
                    row[2] = ent.getEnterpriseType();
                    dtm.addRow(row);
                }
            }
        }

        populateNetworkCombo();
        populateEntTypeCombo();
    }

    public void populateNetworkCombo() {
        DefaultComboBoxModel dcbm = (DefaultComboBoxModel) nwJCombox.getModel();
        nwJCombox.removeAllItems();

        for (Network nw : system.getNetworkList()) {
            nwJCombox.addItem(nw);
        }

    }

    public void populateEntTypeCombo() {
        DefaultComboBoxModel dcbm = (DefaultComboBoxModel) entTypeJCombox.getModel();
        entTypeJCombox.removeAllItems();
        entTypeJCombox.addItem(Enterprise.EnterpriseType.COMMUNITY);
        entTypeJCombox.addItem(Enterprise.EnterpriseType.FINANCE);

    }

