    private void submitEnterpriseJBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitEnterpriseJBtnActionPerformed
        // TODO add your handling code here:
        if (!enterpriseJTxt.getText().isEmpty()) {
            Network selectedNetwork = (Network) nwJCombox.getSelectedItem();
            if (selectedNetwork != null) {
                Boolean flag = false;
                for (Enterprise ent : selectedNetwork.getEnterpriseDirectory().getEnterpriseList()) {
                    if (ent.getName().equalsIgnoreCase(enterpriseJTxt.getText())) {
                        flag = true;
                        break;
                    }
                }
                if (flag == false) {
                    selectedNetwork.getEnterpriseDirectory().createAndAddEnterprise(enterpriseJTxt.getText(), (EnterpriseType) entTypeJCombox.getSelectedItem());
                    JOptionPane.showMessageDialog(null, "Enterprise added successfully!");
                } else {
                    JOptionPane.showMessageDialog(null, "Enterprise already exist!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Please selected Network!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please enter enterprise name!");
        }
        populateEnterprise();
        populateNetworkCombo();
        populateEnterprise();
        enterpriseJTxt.setText("");
    }//GEN-LAST:event_submitEnterpriseJBtnActionPerformed

