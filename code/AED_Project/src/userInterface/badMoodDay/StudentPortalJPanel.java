    public void populateEvaluationWindow() {
        DefaultListModel DLM = new DefaultListModel();
        Boolean newNotification = false;
        for (ChatConversation conversation : ((StudentPortalWorkRequest) this.request).getSolDiscussionList()) {
            DateFormat df = new SimpleDateFormat("MM/dd/yyyy HH:mm:ss");
            String reportDate = df.format(conversation.getDateOfproposal());
            DLM.addElement(conversation.getCoversationId() + " " + reportDate);
            newNotification = true;
        }

        if (newNotification.equals(true)) {
            proposedSolJList.setModel(DLM);
        }

    }

    private void postJBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_postJBtnActionPerformed
        // TODO add your handling code here:
        if (!solutionJTxtArea.getText().isEmpty()) {
            if (this.request != null) {
                this.request.setCheckedNotification(false);
                ChatConversation conversation = new ChatConversation();
                conversation.setProposedSoln(solutionJTxtArea.getText());
                conversation.setReplierUA(acc);
                conversation.setSenderUA(request.getSender());
                conversation.setDateOfReply(null);
                conversation.setReply("");
                conversation.setImpMark(false);
                conversation.setDateOfproposal(new Date());
                ((StudentPortalWorkRequest) this.request).getSolDiscussionList().add(conversation);
                this.request.setStatus("Solution provided");

                JOptionPane.showMessageDialog(null, "Proposed solution sent!");
                populatePortalTable();
                resetProposedSol();
            } else {
                JOptionPane.showMessageDialog(null, "No request selected to propose solution");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please enter your proposed solution!");
        }
    }//GEN-LAST:event_postJBtnActionPerformed

