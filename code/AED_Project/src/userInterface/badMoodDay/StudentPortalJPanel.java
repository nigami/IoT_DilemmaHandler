    public void populatePriorValue() {
        DefaultTableModel dtm = (DefaultTableModel) studentPortalJTable.getModel();
        int selectedRow = studentPortalJTable.getSelectedRow();
        StudentPortalWorkRequest req = (StudentPortalWorkRequest) dtm.getValueAt(selectedRow, 0);
        subjectSolPaneLabel.setText(req.getMessage());
        senderIdJLbl.setText(req.getSender().getUsername());
        dateOfPostJLbl.setText(String.valueOf(req.getRequestDate()));
        todayDateJLbl.setText(String.valueOf(new Date()));
        statusJlabel.setText(req.getStatus());
        QueryJTxtArea.setText(req.getQuery());
        this.request = req;
    }

