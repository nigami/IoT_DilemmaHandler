    public void determineTeacherProfile() {
        for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (org instanceof TeacherOrganization) {
                for (ProfessionalsProfile teacherProfile : ((TeacherOrganization) org).getTeacherDirectory().getTeacherList()) {
                    if (acc == teacherProfile.getUserAccount()) {
                        this.tp = teacherProfile;
                        jProgressBar1.setValue(100);
                        break;
                    }//
                }//
            }//end if org
        }//end for org
    }

    public void populateNotification() {
        DefaultListModel DLM = new DefaultListModel();
        Boolean newNotification = false;
        for (WorkRequest request : org.getWorkQueue().getWorkRequestList()) {
            try {
                if (request.getStatus().equalsIgnoreCase("completed") && request.getCheckedNotification().equals(false)) {
                    DateFormat df = new SimpleDateFormat("MM/dd/yyyy HH:mm:ss");
                    String reportDate = df.format(request.getRequestDate());
                    DLM.addElement(request.getId() + "(" + reportDate + ")" + request.getMessage());
                    newNotification = true;
                }

            } catch (Exception e) {
              //  System.err.println("youthEntryWorkAreaJPanel..........." + e);
            }

        }
        if (newNotification.equals(true)) {
            notificationScrollPane.setVisible(true);
            jList1.setModel(DLM);
        }

    }

