    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        // TODO add your handling code here:
        if (jList1.getSelectedValue() != null) {

            String[] parts = String.valueOf(jList1.getSelectedValue()).split("\\(");
            String[] dateRetrive = parts[1].split("\\)");
            int idMatch = Integer.valueOf(parts[0]);

            for (WorkRequest request : acc.getWorkQueue().getWorkRequestList()) {
                DateFormat df = new SimpleDateFormat("MM/dd/yyyy HH:mm:ss");
                String reportDate = df.format(request.getRequestDate());

                if ((Integer.valueOf(request.getId()) == idMatch) && (reportDate.equals(dateRetrive[0]))) {
                    request.setCheckedNotification(true);
                    if (request instanceof PersonalHealthIssueWorkRequest) {
                        HealthIssueRequestJPanel healthIssueRequestJPanel = new HealthIssueRequestJPanel(userProcessContainer, acc, enterprise, ecosystem);
                        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                        userProcessContainer.add("healthIssueRequestJPanel", healthIssueRequestJPanel);
                        layout.next(userProcessContainer);
                    } else if (request instanceof StudentPortalWorkRequest && ((StudentPortalWorkRequest) request).getCourseId().isEmpty()) {
                        StudentPortalJPanel studentPortalJPanel = new StudentPortalJPanel(userProcessContainer, acc, enterprise, ecosystem);
                        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                        userProcessContainer.add("studentPortalJPanel", studentPortalJPanel);
                        layout.next(userProcessContainer);
                    } else if (request instanceof StudentPortalWorkRequest && (!((StudentPortalWorkRequest) request).getCourseId().isEmpty())) {
                        StudentTeacherMentorPortalJPanel studentTeacherMentorPortalJPanel = new StudentTeacherMentorPortalJPanel(userProcessContainer, acc, enterprise, ecosystem);
                        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                        userProcessContainer.add("studentTeacherMentorPortalJPanel", studentTeacherMentorPortalJPanel);
                        layout.next(userProcessContainer);
                    }
                    break;
                } else {
                    JOptionPane.showMessageDialog(null, "No panel match");
                }
            }
        }
    }//GEN-LAST:event_jList1MouseClicked

