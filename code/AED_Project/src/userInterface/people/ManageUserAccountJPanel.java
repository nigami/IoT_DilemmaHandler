    private void createJBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createJBtnActionPerformed
        // TODO add your handling code here:
        String userName = userprofileJtxt.getText();
        String password = String.valueOf(pwdJField.getPassword());

        if (!userName.isEmpty() && !password.isEmpty() && personJCombox.getSelectedItem() != null && roleJComboBox.getSelectedItem() != null) {
            Organization org = (Organization) orgComboBox.getSelectedItem();
            org.getUserAccountDirectory().createUserAccount(userName, password, (Person) personJCombox.getSelectedItem(), (Role) roleJComboBox.getSelectedItem());
            JOptionPane.showMessageDialog(null, "Profile added successfully");
        } else {
            JOptionPane.showMessageDialog(null, "Please input and select all the fields");
        }

        populateUserAccount();
        populateOrgCombo();
        userprofileJtxt.setText("");
        pwdJField.setText("");
    }//GEN-LAST:event_createJBtnActionPerformed

    private void orgComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orgComboBoxActionPerformed
        // TODO add your handling code here:
        if (orgComboBox != null) {
            Organization org = (Organization) orgComboBox.getSelectedItem();
            roleJComboBox.removeAllItems();
            if (org != null) {
                for (Role role : org.getSupportedRole()) {
                    roleJComboBox.addItem(role);
                }
            }
            populatePersonCombo(org);
        }
    }//GEN-LAST:event_orgComboBoxActionPerformed

