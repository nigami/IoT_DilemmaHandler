    public void populateOrgCombo() {
        orgComboBox.removeAllItems();
        for (Organization org : organizationDir.getOrganizationList()) {
            if (!org.getName().contains("Student")) {
                orgComboBox.addItem(org);
            }
        }
    }

    public void populatePersonCombo(Organization org) {
        personJCombox.removeAllItems();
        if (org != null) {
            for (Person person : org.getPersonDirectory().getPersonList()) {
                personJCombox.addItem(person);
            }
        }

    }

    public void populateUserAccount() {
        DefaultTableModel dtm = (DefaultTableModel) userAccountJTable.getModel();
        dtm.setRowCount(0);

        Object row[] = new Object[2];
        for (Organization org : organizationDir.getOrganizationList()) {
            for (UserAccount userAcc : org.getUserAccountDirectory().getUserAccountList()) {
                row[0] = userAcc;
                row[1] = userAcc.getRole();
                dtm.addRow(row);
            }
        }
    }

