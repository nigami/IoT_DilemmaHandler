    private void removeJBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeJBtnActionPerformed
        // TODO add your handling code here:
        if (userAccountJTable.getSelectedRow() > -1) {
            DefaultTableModel dtm = (DefaultTableModel) userAccountJTable.getModel();
            UserAccount ua = (UserAccount) dtm.getValueAt(userAccountJTable.getSelectedRow(), 0);
            for (Organization org : organizationDir.getOrganizationList()) {
                for (UserAccount userAcc : org.getUserAccountDirectory().getUserAccountList()) {
                    if (userAcc == ua) {
                        org.getUserAccountDirectory().getUserAccountList().remove(ua);
                        break;
                    }
                }
            }
            populateUserAccount();
        }
    }//GEN-LAST:event_removeJBtnActionPerformed

