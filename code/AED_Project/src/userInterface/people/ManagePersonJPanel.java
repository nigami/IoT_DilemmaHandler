 public String ValidateProfile() {

        String errMsg = new String();

        if (nameJTextField1.getText().isEmpty()) {
            errMsg = errMsg + "Name is manadatory\n";
        } else {
            if (!nameJTextField1.getText().matches("[a-zA-Z0-9 ]*")) {
                errMsg = errMsg + "Name is not a valid input\n";
            }
        }

        if (stAddTextArea1.getText().isEmpty()) {
            errMsg = errMsg + "Street Address is manadatory\n";
        } else {
            if (!stAddTextArea1.getText().matches("[a-zA-Z_0-9# .-]*")) {
                errMsg = errMsg + "Street Address is not a valid input\n";
            }
        }

        if (townJText1.getText().isEmpty()) {
            errMsg = errMsg + "Town is manadatory\n";
        } else {
            if (!townJText1.getText().matches("[a-zA-Z_0-9]*")) {
                errMsg = errMsg + "Town is not a valid input\n";
            }
        }
        if (zipJText1.getText().isEmpty()) {
            errMsg = errMsg + "Zipcode is manadatory\n";
        } else {
            if (!zipJText1.getText().matches("[0-9]*")) {
                errMsg = errMsg + "Zipcode can only be integer\n";
            }
            if (!(zipJText1.getText().length() == 5)) {
                errMsg = errMsg + "Zip Code should be 5 digits\n";
            }
        }
       
        if (phNumJText1.getText().isEmpty()) {
            errMsg = errMsg + "Phone Number is manadatory\n";
        } else {
            if (!phNumJText1.getText().matches("[0-9]*")) {
                errMsg = errMsg + "Phone number is integer only\n";
            }
            if (!(phNumJText1.getText().length() == 10)) {
                errMsg = errMsg + "Phone number should be 10 digits\n";
            }
        }

        return errMsg;
    }

    
    
    private void addJButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButton1ActionPerformed
        Organization organization = (Organization) organizationEmpJComboBox.getSelectedItem();
         String errMsg = "";
        errMsg = ValidateProfile();
        if (errMsg.toString().isEmpty() == true) {
                if (organization != null) {

                    Person person = organization.getPersonDirectory().addPerson();
                    person.setName(nameJTextField1.getText());
                    person.setPhoneNum(Long.valueOf(phNumJText1.getText()));
                   // person.setEmailId(emailJText2.getText());
                    person.setStAddress(stAddTextArea1.getText());
                    person.setTown(townJText1.getText());
                    person.setZipCode(Integer.valueOf(zipJText1.getText()));

                    JOptionPane.showMessageDialog(null, "Person added successfully");
                } else {
                    JOptionPane.showMessageDialog(null, "Please enter valid name");
                }
                populateOrganizationComboBox();
                populateOrganizationEmpComboBox();
                nameJTextField1.setText("");
                resetFields();
        } else {
            JOptionPane.showMessageDialog(new JFrame(), errMsg.toString(), "Error Message",
                    JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_addJButton1ActionPerformed

