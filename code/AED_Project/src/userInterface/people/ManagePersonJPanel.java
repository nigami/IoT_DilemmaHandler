    private void removeJBtn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeJBtn2ActionPerformed

        // TODO add your handling code here:
        Organization organization = (Organization) organizationJComboBox.getSelectedItem();
        if (orgPersonJTable.getSelectedRow() > -1) {
            DefaultTableModel dtm = (DefaultTableModel) orgPersonJTable.getModel();
            Person selPerson = (Person) dtm.getValueAt(orgPersonJTable.getSelectedRow(), 1);

            for (Person person : organization.getPersonDirectory().getPersonList()) {
                if (person == selPerson) {
                    for (Organization org : organizationDir.getOrganizationList()) {
                        for (UserAccount userAcc : org.getUserAccountDirectory().getUserAccountList()) {
                            if (userAcc.getPerson() == selPerson) {
                                org.getUserAccountDirectory().getUserAccountList().remove(userAcc);
                                break;
                            }
                        }
                    }
                    organization.getPersonDirectory().getPersonList().remove(selPerson);
                }
            }

        }
        populateTable(organization);

    }//GEN-LAST:event_removeJBtn2ActionPerformed

