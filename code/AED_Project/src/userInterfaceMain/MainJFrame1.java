    public void ReturnAcc(UserAccount acc, Organization inOrganization, Enterprise inEnterprise, Ecosystem system) {
        userAccount = acc;
        //loginLabel.setText("Welcome "+userAccount.getUsername());

        if (userAccount == null) {

            return;
        } else {
            userProcessContainer.removeAll();
            userProcessContainer.setLayout(new CardLayout());
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            JPanel temp = userAccount.getRole().createWorkArea(userProcessContainer, userAccount, inOrganization, inEnterprise, system);
            userProcessContainer.add("workArea", userAccount.getRole().createWorkArea(userProcessContainer, userAccount, inOrganization, inEnterprise, system));
            layout.next(userProcessContainer);
            loginJMenu.getMenuComponent(0).setEnabled(false);
            loginJMenu.getMenuComponent(1).setEnabled(true);
            signUpJMenuItem.setVisible(false);

        }

    }

