    public void signUp() {
        Enterprise inEnterprise = null;
        Organization inOrganization = null;
        userAccount = new UserAccount();

        //Step2: Go inside each network to check each enterprise
        for (Network network : ecoSystem.getNetworkList()) {
            //Step 2-a: Check against each enterprise
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {

                //Step3: Check against each organization inside that enterprise
                for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    //userAccount = organization.getUserAccountDirectory().authenticateUser(userName, password);
                    if (organization instanceof StudentOrganization) {
                        inEnterprise = enterprise;
                        inOrganization = organization;
                        userAccount.setUsername("student");
                        userAccount.setPassword("student");
                        userAccount.setRole(new StudentRole());
                        //  userAccount.setPerson(new Person());
                        break;
                    }
                }

                if (inOrganization != null) {
                    break;
                }
            }
            if (inEnterprise != null) {
                break;
            }
        }

        if (userAccount == null) {

            JOptionPane.showMessageDialog(null, "Soory!No Student Organization present yet!");
        } else {
            userProcessContainer.removeAll();
            userProcessContainer.setLayout(new CardLayout());
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            JPanel temp = userAccount.getRole().createWorkArea(userProcessContainer, userAccount, inOrganization, inEnterprise, ecoSystem);
            userProcessContainer.add("workArea", userAccount.getRole().createWorkArea(userProcessContainer, userAccount, inOrganization, inEnterprise, ecoSystem));
            layout.next(userProcessContainer);

        }

    }
